    private boolean validar() {
        if (txtnombre.getText().equals("") && txtNit.getText().equals("")
                && txtDireccion.getText().equals("") && txtTelefono.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "POR FAVOR LLENE TODOS LOS CAMPOS", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtnombre.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "POR FAVOR INGRESE EL NOMBRE DEL PROVEEDOR", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtNit.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "POR FAVOR INGRESE EL NIT DEL PROVEEDOR", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtDireccion.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "POR FAVOR INGRESE LA DIRECCIÓN DEL PROVEEDOR", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (txtTelefono.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "POR FAVOR INGRESE EL TELEFONO DEL PROVEEDOR", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    private void limpiar() {
        txtnombre.setText("");
        txtNit.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
    }

    private void cargarProveedores() {
        proveedores = Fachada.getInstancia().obtenerProveedores();
        DefaultTableModel modelo = (DefaultTableModel) tablaProveedores.getModel();
        modelo.setRowCount(0);
        if (proveedores.size() != 0) {
            for (Proveedor proveedore : proveedores) {

                Object[] fila = new Object[5];
                fila[0] = proveedore.getId();
                fila[1] = proveedore.getNombre();
                fila[2] = proveedore.getNit();
                fila[3] = proveedore.getDireccion();
                fila[4] = proveedore.getTelefono();
                modelo.addRow(fila);

            }
            tablaProveedores.setModel(modelo);
        }
    }

    private void cargarProveedor() {
        txtnombre.setText(proveedor.getNombre());
        txtNit.setText(proveedor.getNit());
        txtDireccion.setText(proveedor.getDireccion());
        txtTelefono.setText(proveedor.getTelefono());
    }

