    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (validar()) {
            String nombre = txtnombre.getText();
            String nit = txtNit.getText();
            String direccion = txtDireccion.getText();
            String telefono = txtTelefono.getText();
            proveedor = new Proveedor(nombre, nit, direccion, telefono);
            if (Fachada.getInstancia().registrarProveedor(proveedor)) {
                JOptionPane.showMessageDialog(this, "Se ha registrados un nuevo proveedor",
                        "Registro Exito!!", JOptionPane.INFORMATION_MESSAGE);
                cargarProveedores();
                limpiar();
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (validar()) {
            proveedor.setNombre(txtnombre.getText());
            proveedor.setNit(txtNit.getText());
            proveedor.setDireccion(txtDireccion.getText());
            proveedor.setTelefono(txtTelefono.getText());
            if (Fachada.getInstancia().actualizarProveedor(proveedor)) {
                JOptionPane.showMessageDialog(this, "Se ha modificado el proveedor",
                        "Modificación Exitosa!!", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
                btnRegistrar.setEnabled(true);
            }

        }
    }//GEN-LAST:event_btnActualizarActionPerformed

